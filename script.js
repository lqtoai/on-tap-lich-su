let currentIdx = 0;

function loadQuestion() {
    const question = questions[currentIdx];
    document.getElementById('question-text').innerText = question.q;
    document.getElementById('question-number').innerText = `Câu ${currentIdx + 1}/${questions.length}`;
    
    const container = document.getElementById('options-container');
    container.innerHTML = '';
    
    question.options.forEach((opt, i) => {
        const div = document.createElement('div');
        div.className = 'option-card';
        div.innerText = opt;
        div.onclick = () => checkAnswer(i, div);
        container.appendChild(div);
    });

    document.getElementById('next-btn').style.display = 'none';
    updateProgressBar();
}

function checkAnswer(selectedIdx, element) {
    const correctIdx = questions[currentIdx].answer;
    const cards = document.querySelectorAll('.option-card');
    
    cards.forEach(card => card.style.pointerEvents = 'none'); // Khóa nhấn tiếp

    if (selectedIdx === correctIdx) {
        element.classList.add('correct');
    } else {
        element.classList.add('wrong');
        cards[correctIdx].classList.add('correct');
    }
    
    document.getElementById('next-btn').style.display = 'block';
}

function updateProgressBar() {
    const progress = ((currentIdx) / questions.length) * 100;
    document.getElementById('progress-bar').style.width = `${progress}%`;
}

document.getElementById('next-btn').onclick = () => {
    currentIdx++;
    if (currentIdx < questions.length) {
        loadQuestion();
    } else {
        alert("Chúc mừng! Bạn đã hoàn thành bài ôn tập.");
    }
};
// Thêm biến để lưu câu trả lời của người dùng
let userAnswers = new Array(questions.length).fill(null);

function loadQuestion() {
    const question = questions[currentIdx];
    document.getElementById('question-text').innerText = question.q;
    document.getElementById('question-number').innerText = `Câu ${currentIdx + 1}/${questions.length}`;
    
    const container = document.getElementById('options-container');
    container.innerHTML = '';
    
    // Hiển thị/Ẩn nút Quay lại
    document.getElementById('prev-btn').style.display = currentIdx > 0 ? 'block' : 'none';
    
    question.options.forEach((opt, i) => {
        const div = document.createElement('div');
        div.className = 'option-card';
        div.innerText = opt;
        
        // Nếu đã trả lời rồi thì hiển thị lại trạng thái đúng/sai
        if (userAnswers[currentIdx] !== null) {
            div.style.pointerEvents = 'none'; // Khóa click
            if (i === question.answer) {
                div.classList.add('correct');
            } else if (i === userAnswers[currentIdx]) {
                div.classList.add('wrong');
            }
            document.getElementById('next-btn').style.display = 'block';
        } else {
            div.onclick = () => checkAnswer(i, div);
            document.getElementById('next-btn').style.display = 'none';
        }
        
        container.appendChild(div);
    });

    updateProgressBar();
}

function checkAnswer(selectedIdx, element) {
    const correctIdx = questions[currentIdx].answer;
    userAnswers[currentIdx] = selectedIdx; // Lưu lại đáp án đã chọn
    
    const cards = document.querySelectorAll('.option-card');
    cards.forEach(card => card.style.pointerEvents = 'none');

    if (selectedIdx === correctIdx) {
        element.classList.add('correct');
    } else {
        element.classList.add('wrong');
        cards[correctIdx].classList.add('correct');
    }
    
    document.getElementById('next-btn').style.display = 'block';
}

// Xử lý sự kiện nút Quay lại
document.getElementById('prev-btn').onclick = () => {
    if (currentIdx > 0) {
        currentIdx--;
        loadQuestion();
    }
};

// Xử lý sự kiện nút Tiếp theo
document.getElementById('next-btn').onclick = () => {
    if (currentIdx < questions.length - 1) {
        currentIdx++;
        loadQuestion();
    } else {
        alert("Bạn đã hoàn thành bộ câu hỏi!");
    }
};
